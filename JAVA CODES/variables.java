public class variables
{
    public static void main(String[] args) 
    {
        int x;
        float y;
        char z;
        boolean b;
        String s;   //String is non-primitive in Java and is declared in this way. 
        
        x = 10;
        y = 20.5f;
        z = 'A';
        b = true;
        s="YOOOOOO";
        // Print the values of the variables
        System.out.println("\nValue of x:"+x+"\nValue of y:"+y+"\nCharacter:"+z+"\nboolean:"+b+"\nstring:"+s);
    }
}
