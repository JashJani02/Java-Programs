public class String1
{
    public static  void main (String[] args)
    {
        /*
        Charat function:-
            charat function is used to print a character in a string at a certain index
        */
        String name = "Jash Jani";
        String uni = "Marwadi Univeristy";
        System.out.println("hi "+name+"\n "+name.charAt(0)+name.charAt(1)+"\n "+name.length()+"\n "+name.startsWith("J",0)+"\n"+uni.charAt(3));
    }
}